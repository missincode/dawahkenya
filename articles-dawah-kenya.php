  <h3>The Islamic Financial System</h3>

<p>While elimination of "Riba" or interest in all its forms is an important feature of the Islamic financial system, Islamic banking is much more. At the heart of Islam is a sense of cooperation, to help one another according to principles of goodness and piety (but not to cooperate in evil or malice). In essence, it aims to eliminate exploitation and to establish a just society by the application of the Shari'ah or Islamic rulings to the operations of banks and other financial institutions. To ensure compliance to the Shari'ah, Islamic banks use the services of religious boards comprised of Shari'ah scholars.</p>

<p>Islamic finance may be viewed as a form of ethical investing, or ethical lending, except that no loans are possible unless they are interest-free. Among the ethical restrictions is the prohibition on alcohol and gambling and the consumption of pork. Islamic funds would never knowingly invest in companies involved in gambling, alcoholic beverages, or porcine food products.</p>

<p>Its practitioners and clients need not be Muslim, but they must accept the ethical restrictions underscored by Islamic values.</p>
 
 
<h4>The Concepts</h4>

<p>Islamic economic principles offers a balance between extreme capitalism and communism. It offers the individual the freedom to produce and create wealth, while surrounding the individual with an environment controlled, not by human rulers, but by Divine Guidance, which sets moral rules and norms of behaviour that must require the utmost sincerity of intention. When these rules and norms are internalised and acted upon by people, peace and prosperity result for the wider society.</p>

<p>The Qur'an (2:30) says that man was created as the representative of God on earth. This concept has a considerable effect on Islamic business, since the lack of a sense of absolute ownership promotes a sense working for society, especially the needy.</p>

<p>This is not some philosophical concept, removed from the daily life of the society. It manifests itself in all the different aspects of lives. What makes the trader, banker, agriculturist or research and development scientist perform his job to the best of his ability? In capitalist economies, it is the notion of competition. This involves the necessity to constantly produce more new things for profit to keep up with others and this makes for wastage and often generates unbridled greed. But in an economy based on Islamic principles, the idea of man representing God on earth gives businessmen a feeling of co-operating with others for the good of society as a whole, including himself. Thus Quranic guidance enables man to conserve and use prudently all the resources of the earth that God has given mankind.</p>


<h3>The Essentials</h3>

<p>Divine Guidance for the economy, as enshrined in the Qur'an and the Sunnah (the living example of Prophet Muhammad), can be summarised as follows:</p>

<ol>
<li>Trusteeship

<p>The Qur'an (57:7) emphasises that all the resources of the earth belong to God, the Creator, who has made human beings a trustee for them. Humans are therefore accountable to God for the uses they make of these resources. The idea of trusteeship distinguishes the Islamic approach to economics from materialistic approaches such as extreme capitalism and socialism. It introduces a moral and spiritual element into business life and has been made practicable by creating rules to govern individual behaviour and public policy.</p></li>

<li>Care For Others

<p>Care for others tempers self-interest, which is ingrained in human nature. It goes naturally with trusteeship, since, in caring for others, one also serves God, who created all humans. No one can have fulfilment or happiness in his life without interacting with others. Thus individual happiness and collective interests go hand in hand.</p></li>

<p>We gain through giving, since it would be impossible for everyone to acquire while giving nothing. The Qur'an states this in 30:39 and 2:276. It follows that Islam discourages indulgence in luxuries. One is expected to consider what is available to others before acquiring good things for oneself. Moderation in consumption is mentioned in the Qur'an 7:31.</p>

<p>People who believe that they can increase their wealth through charging others interest and by reducing charitable giving are under an illusion. The wealth and integrity of a society can only increase when the rich give part of their wealth to the needy for no other motivation than to please God. Those who have faith and a vision of their future life understand this.</p>

<p>To think only of how to gain profit for oneself leads to using others as mere instruments. In societies where unbridled self-interest is allowed to dominate unchecked, there is no protection for the weak against the strong. Thus exclusive pursuit of self-interest, when not tempered by charity, is self- defeating.</p>

<li>Productive Effort as a Means of Serving God

<p>Islam emphasises the duty of every individual to work for his living. Productive enterprise is looked upon as a means of serving God (2:195).</p>

<p>Islam requires wealth to be spent in the cause of God. This realisation moves Muslims to greater efforts in their economic activities. The fourteenth-century thinker Abu Ishaq Shatibi, writing of the companions of the Prophet, said,

                <div class="w3-container w3-padding-64">
                <p><q>"They were expert in business enterprise, keen and persistent in a variety of economic pursuits. They did not do so to amass wealth or save it for themselves; rather their aim was to spend their earnings in good causes.”</q></p> <p>(Shatibi, Al-Muwafiqaat fi Usul al-Shari'ah, Vol. 2, p188, Cairo, Maktaba al Tijarah al-Kubra.)</p>
                </div>
   
                            
    

<p>In the west, it is now considered enough to merely to ‘enjoy life', work being an unfortunate necessity. But in Islam, it is seen that working for a living gives man a sense of worthiness in his society. To support a family and contribute to others with any surplus enables one to take one's part in consultations on practical, social matters, so that all can benefit.</p></li>

<li>Application of the Shari'ah Rulings to Business

<p>The aim of the Shari'ah rulings is to make the transfer of goods safe and easy and to facilitate economic transactions by eliminating vagueness or misunderstanding in all types of contracts. It prohibits the charging of interest on loans as a form of injustice. The goal is to remove the causes of social tension or litigation and to promote a climate of peace and goodwill. Islam strongly recommends that the terms of financial agreements be put in writing.</p></li>


<li>Mutual Consultation 
<p>Men are free to make private economic decisions, but decisions concerning the public welfare must be based on consultation. The Qur'an describes Muslims as a people "whose rule (in all matters of common concern) is by consultation among themselves.” (42:39). Mutual consultation avoids society or local communities coming under the rule of a dictator and makes sure that reasonable decisions acceptable to all are made.</p></li>
 

<li>Treating Wealth as a Means and not an End

<p>Islam regards economic well being as a means to peace, freedom from hunger and freedom from fear of others, except God. Beyond the satisfaction of basic needs, the ultimate objectives of earning and spending money are moral and spiritual. It is against Islamic rationality to hoard money (9:34, 35).</p>

<p>It follows that savings must be put to good use. One who cannot go into business himself can do so in partnership with others, or can supply funds on a profit-sharing basis. People can also borrow and lend, but it is forbidden for the lender to claim interest from the borrower as this is unjust (2:275). Islam prohibits gambling, cheating, exploitation, coercion, etc., but freedom to make financial arrangements is constrained only by these few prohibitions and by the Islamic tendency to treat money as a means to the good life.</p></li></ol>

<h3>Proper Functioning of the Market</h3>

<p>Islam prohibits dishonesty, fraud and deception, coercive practices, gambling and usurious and injurious dealings. Hoarding, speculation and collusion among producers and traders against the interest of consumers, and such monopolies as are injurious to the socio-economic health of society are all ruled out. The basic principles regulating market operations in an Islamic state are:</p>
<ol type="a">
<li>A person should be free to buy, sell or dispose of his possessions and money within the framework of the Shari'ah.</li>

<li>There is no restriction on the percentage of profit which a trader may make. It is left to him and depends on the business environment and the nature of the goods. However, moderation, contentment and leniency must be taken into consideration.</li>

<li>The Shari'ah emphasises avoiding illicit acts detrimental to the wellbeing of society or the individual.</li>
<li>The State should not fix prices except where there are artificial factors in the market which may lead to excessive price increases or decreases or fraud. If there are such, the State should intervene to remove these factors.</li></ol>
 

<p>7. Protection of Consumers
The State should insure that producers, manufacturers and traders do not exploit each other or the buyers. It should curb adulteration, under-weighing, encroachment of thoroughfares, unhealthy trades and unlawful professions and maintain good, firm employee relationships.</p>
 

<p>8. Monopolies and Cartels

Industrialists in a free and competitive economy can form cartels and monopolies and exploit people and a firm law is needed to control them. No unjust, oppressive or cheating business can be allowed to continue in an Islamic economy.</p>


<p>9. Zakat or Zakah

Zakat is a levy on certain categories of wealth. It can be collected and distributed by the government and is obligatory only on Muslims. It is applicable to income and savings, agricultural harvests, commercial goods, gold and silver over certain amounts, some categories of livestock, excavated treasures, mined wealth, etc.</p>

<p>In accordance with the Qur'an (9:60), the proceeds from zakat are paid to the poor, the sick and destitute and to travellers, especially those seeking education or going on pilgrimage.

<p>The Islamic view of distributive justice is contained in the three points: a guarantee of the fulfilment of basic needs; equality of opportunity; and elimination of glaring inequalities in personal income and wealth. Zakat also acts as an excellent form of social insurance.


<p>10. Qard Hasan

Qard hasan is a Quranic term meaning an interest-free loan. It was the primary source of financing introduced by the Prophet after entering Medina and was used primarily for productive economic purposes, such as setting up qualified, but poor, people in trade and agriculture.</p>
